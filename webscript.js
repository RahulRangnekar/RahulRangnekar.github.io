/* PI SIGMA EPSILON */

var trackPSE = 'o';

function hoverPSE() {
	var image = document.getElementById('PSE-pic');
	if (trackPSE == 'o') {
		image.src = "images/PSE-hover.jpg";
		trackPSE = 'h';
	}
}

function clickPSE() {
	var image = document.getElementById("PSE-pic");
	if (trackPSE == 'd') {
		image.src = "images/PSE.jpg";
		trackPSE = 'o';
	} else {
		image.src = "images/PSE-desc.jpg";
		trackPSE = 'd';
	}
}

function offPSE() {
	var image = document.getElementById("PSE-pic");
	if (trackPSE == 'h' || trackPSE == 'o') {
		image.src = "images/PSE.jpg";
		trackPSE = 'o';
	} else {
		image.src = "images/PSE-desc.jpg";
		trackPSE = 'd';
	}
}

